package com.hakstrace.common.code.domain;

public class CommonCode {

	private String value;
	private String text;
	
	public CommonCode(){
		
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
	
	
}
